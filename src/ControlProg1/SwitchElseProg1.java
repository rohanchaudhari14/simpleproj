package ControlProg1;

public class SwitchElseProg1 {

	public static void main(String[] args) {
		
		String day = "saturday";
		switch(day)
		{
			case "monday":
				System.out.println("today is monday");
				break;
				
			
			case "Tusday":
				System.out.println("Today is tusday");
				break;
				
			case "wednsday":
				System.out.println("today is wednsday");
				break;
			case "Thusday":
				System.out.println("Today is thusday");
				break;
			case "Friday":
				System.out.println("Today is friday");
				break;
				default:
					System.out.println("wow its weekend!");
		}
		System.out.println("switch is finish");
		
				
				
			
		}
	}


